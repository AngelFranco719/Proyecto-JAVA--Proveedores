    private void B_ConfirmarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_B_ConfirmarMouseClicked
       try{
           String Query="INSERT INTO Compra VALUES(?,?,?,?,?,?)";
           PreparedStatement stm=Conexion_Actual.getConnection().prepareStatement(Query);
           stm.setInt(1,ID_Actual);
           stm.setInt(2,Integer.parseInt(Factura_Actual.get(0)));
           stm.setInt(3,Integer.parseInt(Oferta_Actual.get(0)));
           stm.setInt(4,numero);
           stm.setFloat(5,subtotal); 
           stm.setInt(6,cantidad);
           Conexion_Actual.EjecutarSentencia(stm);
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,"Error al Ingresar el Registro");
       } 
       Confirmacion_Compra.this.dispose();
    }//GEN-LAST:event_B_ConfirmarMouseClicked

    private String ObtenerProducto(){
        int ID_Oferta=Integer.parseInt(this.Oferta_Actual.get(0));
        String Query="SELECT Pieza.Pie_Nombre AS 'Pieza' FROM Pieza " +
        "INNER JOIN Oferta ON Oferta.ID_Pieza=Pieza.ID_Pieza " +
        "WHERE Oferta.ID_Oferta="+ID_Oferta;
        String Producto=this.Conexion_Actual.getUniqueResultQuery(Query);
        return Producto; 
    }
